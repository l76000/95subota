function getTimetables() {
  const now = new Date();
  const belgradeDateString = now.toLocaleString('en-US', { timeZone: 'Europe/Belgrade' });
  const belgradeDate = new Date(belgradeDateString);
  const dayOfWeek = belgradeDate.getDay();

  console.log(`üìÖ Dan u nedelji: ${dayOfWeek} (0=Ned, 1=Pon, 6=Sub)`);

  if (dayOfWeek === 0) {
    console.log('üìã Red vo≈ænje: NEDELJA');
    return { mapA: timetableMapA_Nedelja, mapB: timetableMapB_Nedelja };
  } else if (dayOfWeek === 6) {
    console.log('üìã Red vo≈ænje: SUBOTA');
    return { mapA: timetableMapA_Subota, mapB: timetableMapB_Subota };
  } else {
    console.log('üìã Red vo≈ænje: RADNI DAN');
    return { mapA: timetableMapA_Radni, mapB: timetableMapB_Radni };
  }
}

export async function getGoogleSheetsClient() {
  const auth = new google.auth.GoogleAuth({
    credentials: {
      client_email: process.env.GOOGLE_SERVICE_ACCOUNT_EMAIL,
      private_key: process.env.GOOGLE_PRIVATE_KEY.replace(/\\n/g, '\n'),
    },
    scopes: ['https://www.googleapis.com/auth/spreadsheets'],
  });

  const authClient = await auth.getClient();
  return google.sheets({ version: 'v4', auth: authClient });
}

export function parseBusLogicData(data) {
  if (!Array.isArray(data)) {
    return [];
  }

  const { mapA, mapB } = getTimetables();
  const currentVehiclesByBrojPolaska = new Map();
  
  // ========================================
  // NOVA LOGIKA: Detektuj duplikate
  // ========================================
  const tempStorage = new Map(); // Kljuƒç: "brojPolaska_startTime_direction", Vrednost: [vozila]

  for (let i = 0; i < data.length; i++) {
    const item = data[i];
    
    const trip = item?.vehicle?.trip;
    const vehicle = item?.vehicle?.vehicle;
    
    if (!trip || !vehicle) continue;
    
    if (trip.lineNumber !== "95") {
      continue;
    }
    
    const routeId = trip.routeId;
    const startTime = trip.startTime;
    const vehicleId = vehicle.id;
    
    if (!routeId || !startTime || !vehicleId) continue;
    
    if (!String(vehicleId).startsWith('P9')) {
      continue;
    }
    
    // Odreƒëivanje smera
    let directionPrefix;
    const tripId = trip.tripId;
    if (tripId && (String(tripId).startsWith('8170') || String(tripId).startsWith('8171'))) {
      directionPrefix = String(tripId).split('_')[0];
    } else if (routeId === '6185') {
      directionPrefix = '8170'; // Smer A
    } else if (routeId === '6186') {
      directionPrefix = '8171'; // Smer B
    } else {
      continue;
    }
    
    const mapToUse = directionPrefix === '8170' ? mapA : mapB;
    const brojPolaska = mapToUse[startTime];
    
    if (!brojPolaska) {
      continue;
    }
    
    const vozilo = String(vehicleId).substring(2);
    
    // Kreiraj jedinstveni kljuƒç za detekciju duplikata
    // Format: "brojPolaska_startTime_direction"
    const uniqueKey = `${brojPolaska}_${startTime}_${directionPrefix}`;
    
    if (!tempStorage.has(uniqueKey)) {
      tempStorage.set(uniqueKey, []);
    }
    
    // Dodaj vozilo u temp storage
    tempStorage.get(uniqueKey).push({
      brojPolaska,
      vozilo,
      vreme: startTime,
      direction: directionPrefix
    });
  }

  // ========================================
  // FILTRIRANJE: Ukloni duplikate
  // ========================================
  let duplicatesFiltered = 0;
  
  for (const [key, vehicles] of tempStorage.entries()) {
    if (vehicles.length > 1) {
      // DUPLIKAT PRONAƒêEN!
      duplicatesFiltered++;
      console.log(`‚ö†Ô∏è DUPLIKAT DETEKTOVAN: ${key}`);
      console.log(`   Vozila: ${vehicles.map(v => v.vozilo).join(', ')}`);
      console.log(`   ‚ùå Preskaƒçem ovaj polazak (ne dodajem u rezultate)`);
      // NE dodajemo ni≈°ta u currentVehiclesByBrojPolaska
    } else if (vehicles.length === 1) {
      // Samo jedno vozilo, sve je OK
      const vehicle = vehicles[0];
      const existing = currentVehiclesByBrojPolaska.get(vehicle.brojPolaska);
      
      if (!existing || vehicle.vreme > existing.vreme) {
        currentVehiclesByBrojPolaska.set(vehicle.brojPolaska, {
          brojPolaska: vehicle.brojPolaska,
          vozilo: vehicle.vozilo,
          vreme: vehicle.vreme
        });
      }
    }
  }

  if (duplicatesFiltered > 0) {
    console.log(`üö´ Ukupno filtrirano duplikata: ${duplicatesFiltered}`);
  }
  
  console.log(`‚úÖ Parsirano ${currentVehiclesByBrojPolaska.size} vozila`);
  return Array.from(currentVehiclesByBrojPolaska.values());
}

export async function updateSheetData(sheets, liveVehicles) {
  const res = await sheets.spreadsheets.values.get({
    spreadsheetId: SPREADSHEET_ID,
    range: FULL_RANGE,
  });

  const rows = res.data.values || [];
  const header = rows.shift() || ["Brojpolaska", "Vozilo", "Vremepolaska", "Zamena 1", "Zamena 2", "Zamena 3"];

  const sheetState = new Map();
  for (let i = 0; i < rows.length; i++) {
    const row = rows[i];
    const brojPolaska = row[0];
    
    if (brojPolaska) {
      sheetState.set(String(brojPolaska), {
        rowIndex: i + 2, 
        brojPolaska: row[0],
        vozilo: row[1],
        vreme: row[2],
        zamena1: row[3] || null,
        zamena2: row[4] || null,
        zamena3: row[5] || null,
      });
    }
  }

  const updateRequests = [];
  const appendRequests = [];

  for (const vehicle of liveVehicles) {
    const existingEntry = sheetState.get(String(vehicle.brojPolaska));
    const novoVozilo = vehicle.vozilo;

    if (!existingEntry) {
      appendRequests.push([
        vehicle.brojPolaska,
        novoVozilo,
        vehicle.vreme,
        "", "", ""
      ]);
      
      sheetState.set(String(vehicle.brojPolaska), { 
        rowIndex: -1, 
        brojPolaska: vehicle.brojPolaska,
        vozilo: novoVozilo,
        vreme: vehicle.vreme
      });
    } else {
      const { rowIndex, vozilo, vreme, zamena1, zamena2, zamena3 } = existingEntry;
      
      if (vehicle.vreme !== vreme) {
        updateRequests.push({
          range: `${SHEET_NAME}!C${rowIndex}`,
          values: [[vehicle.vreme]],
        });
      }
      
      const lastKnownVehicle = zamena3 || zamena2 || zamena1 || vozilo;

      if (lastKnownVehicle !== novoVozilo) {
        let updateColumn = null;
        if (!zamena1) updateColumn = 'D';
        else if (!zamena2) updateColumn = 'E';
        else if (!zamena3) updateColumn = 'F';

        if (updateColumn) {
          updateRequests.push({
            range: `${SHEET_NAME}!${updateColumn}${rowIndex}`,
            values: [[novoVozilo]],
          });
          
          if (updateColumn === 'D') existingEntry.zamena1 = novoVozilo;
          else if (updateColumn === 'E') existingEntry.zamena2 = novoVozilo;
          else if (updateColumn === 'F') existingEntry.zamena3 = novoVozilo;
        }
      }
    }
  }

  if (appendRequests.length > 0) {
    await sheets.spreadsheets.values.append({
      spreadsheetId: SPREADSHEET_ID,
      range: `${SHEET_NAME}!A:F`,
      valueInputOption: 'USER_ENTERED',
      resource: {
        values: appendRequests,
      },
    });
  }

  if (updateRequests.length > 0) {
    await sheets.spreadsheets.values.batchUpdate({
      spreadsheetId: SPREADSHEET_ID,
      resource: {
        valueInputOption: 'USER_ENTERED',
        data: updateRequests,
      },
    });
  }

  const finalRes = await sheets.spreadsheets.values.get({
    spreadsheetId: SPREADSHEET_ID,
    range: FULL_RANGE,
  });
  
  const finalRows = finalRes.data.values || [];
  if (finalRows.length > 0) finalRows.shift(); 
  
  return { header, rows: finalRows };
}
